/** Coworking feature card — viewBox 400×311 */
export function generateCoworkingCardClipPath(W: number, H: number): string {
  const sx = (x: number) => (x * W) / 400;
  const sy = (y: number) => (y * H) / 311;
  const p = (x: number, y: number) => `${sx(x)} ${sy(y)}`;
  return [
    `M ${p(400, 54)}`,
    `C ${p(400, 62.8366)} ${p(392.837, 70)} ${p(384, 70)}`,
    `C ${p(375.163, 70)} ${p(368, 77.1634)} ${p(368, 86)}`,
    `V ${sy(164)}`,
    `C ${p(368, 172.837)} ${p(375.163, 180)} ${p(384, 180)}`,
    `C ${p(392.837, 180)} ${p(400, 187.163)} ${p(400, 196)}`,
    `V ${sy(280)}`,
    `C ${p(400, 288.837)} ${p(392.837, 296)} ${p(384, 296)}`,
    `H ${sx(337.5)}`,
    `C ${p(333.358, 296)} ${p(330, 299.358)} ${p(330, 303.5)}`,
    `C ${p(330, 307.642)} ${p(326.642, 311)} ${p(322.5, 311)}`,
    `H ${sx(16)}`,
    `C ${p(7.16345, 311)} ${p(0, 303.837)} ${p(0, 295)}`,
    `V ${sy(16)}`,
    `C ${p(0, 7.16344)} ${p(7.16344, 0)} ${p(16, 0)}`,
    `H ${sx(384)}`,
    `C ${p(392.837, 0)} ${p(400, 7.16344)} ${p(400, 16)}`,
    `V ${sy(54)}`,
    `Z`,
  ].join(" ");
}
